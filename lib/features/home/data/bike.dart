class Bike {
  const Bike({
    required this.name,
    required this.price,
    required this.rating,
    required this.description,
    required this.topSpeed,
    required this.engine,
    required this.weight,
    required this.image,
  });
  final String name;
  final int price;
  final double rating;
  final String description;
  final int topSpeed;
  final int engine;
  final int weight;
  final String image;
}
